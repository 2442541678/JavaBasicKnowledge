package com.tangwh.fors;

public class forDemo {

    public static void main(String[] args) {
        /**
         * for 循环(初始值;重点判断条件;变化量){
         *
         *  System.out.println("跑了第"+i+"圈了");
         * }
         */
        for (int i=0;i<5;i++){
            System.out.println("跑了第"+i+"圈了");
        }

    }
}
